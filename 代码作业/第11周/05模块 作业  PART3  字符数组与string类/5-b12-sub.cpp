int tj_strstr(const char str[], const char substr[])
{
    /* 注意：函数内不允许定义任何形式的数组（包括静态数组） */
    int len1 = tj_strlen(str);
    int len2 = tj_strlen(substr);

    if (len2 > len1) {
        return 0;
    }

    for (int i = 0; i <= len1 - len2; i += 1) {
        int j = 0;
        while (j < len2 && str[i + j] == substr[j]) {
            j++;
        }
        if (j == len2) {
            return i + 1;
        }
    }
    return 0; //return值可根据需要修改
}

